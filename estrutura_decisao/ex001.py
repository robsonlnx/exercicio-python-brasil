'''Faça um Programa que peça dois números e imprima o maior deles.'''


primeiro_numero = float(input('Primeiro número: '))
segundo_numero = float(input('Segundo número: '))

if primeiro_numero > segundo_numero:
    print(f'O número {primeiro_numero} é maior. ')
elif segundo_numero > primeiro_numero:
    print(f'O número {segundo_numero} é maior. ')
else:
    print('Não existe maior. Números iguais')










